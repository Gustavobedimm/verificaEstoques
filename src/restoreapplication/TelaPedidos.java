public void consultaPedido(String empresa, String numeroPedido) {
        PedidoDAO pedidoDAO = new PedidoDAO();
        Pedido pedido = new Pedido();
        try {
            pedido = pedidoDAO.consultaPedido(numeroPedido, empresa);
            if(pedido.getSTATUS().equals("CAN")){
            jTextFieldStatus.setForeground(Color.red);
            jLabelDescricao.setForeground(Color.red);
            }
            jTextFieldEmpresa.setText(pedido.getEMPRESA());
            jTextFieldCodigo.setText(pedido.getCODIGO());
            jTextFieldData.setText(pedido.getDATA());
            jTextFieldHora.setText(pedido.getHORA());
            jTextFieldAlmox.setText(pedido.getALMOX());
            jTextFieldStatus.setText(pedido.getSTATUS());
            jTextFieldNota.setText(pedido.getNOTAFISCAL());
            jTextFieldVersao.setText(pedido.getVERSAO());
            jTextFieldDescricaoNat.setText(pedido.getDESCRICAO());
            jTextFieldCfop.setText(pedido.getCODIGOFISCAL());
            jTextFieldMovimenta.setText(pedido.getMOVIMENTAESTOQUE());
            jTextFieldDataEfe.setText(pedido.getDATAEFE());
            jTextFieldHoraEfe.setText(pedido.getHORAEFE());
            jTextFieldDataSaida.setText(pedido.getDATASAIDA());
            jTextFieldHoraSaida.setText(pedido.getHORASAIDA());
        } catch (ClassNotFoundException ex) {
            System.out.println(ex);
        }
        ProdutoDAO produtoDAO = new ProdutoDAO();
        //PRODUTOS PEDIDO
        ArrayList<Produto> listaProdutos = new ArrayList();
        DefaultTableModel model = (DefaultTableModel) jTableProdutos.getModel();
        try {
            listaProdutos = produtoDAO.produtosDoPedido(numeroPedido, empresa);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TelaPedidos.class.getName()).log(Level.SEVERE, null, ex);
        }
        for (Produto produto : listaProdutos) {
            model.addRow(new Object[]{empresa,produto.getALMOX(),produto.getCODIGO(),produto.getDESCRICAO(),produto.getQTDE()});
        }
    }
